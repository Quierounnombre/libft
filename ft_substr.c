#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	static size_t	i;
	static size_t	j;
	char			*str;

	str = (char *)malloc((len + 1) * sizeof(char));
	if (!str)
		return (NULL);
	while (s[i])
	{
		if (i >= start && j < len)
		{
			str[j] = s[i];
			j++;
		}
		i++;
	}
	str[j] = 0;
	return (str);
}
