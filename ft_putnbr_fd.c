#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*s;

	if (!n || !fd)
		return ;
	s = "A";
	if (n < 0)
	{
		write(fd, "-", 1);
		n = n * -1;
	}
	while (n != 0)
	{
		*s = (n / 10) + '0';
		write(fd, s, 1);
		n = n % 10;
	}
}
